    private void jButtonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearActionPerformed
       aut_id=controladorAuto.buscarAutoCodigo(opcionComboBuscarAuto);
       java.util.Date fin = new java.util.Date();
       java.sql.Date fin2 = new java.sql.Date(fin.getTime());
       Formulario form= new Formulario();
       
       form.setForm_id(Integer.parseInt(txt_codigo.getText()));
       form.setIncio_fecha(fin2);
       
       form.setDescripcion(txt_area.getText());
       form.setAut_id(aut_id);
       
        if(controladorFormulario.anadirFormulario(form,nombreSucur)==true){
           JOptionPane.showMessageDialog(null,"Creado", "Informacion", JOptionPane.INFORMATION_MESSAGE);
           dispose();
        }
       
    }//GEN-LAST:event_jButtonCrearActionPerformed

    
